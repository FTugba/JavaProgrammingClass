package week03;

public class UnaryReview {
    public static void main(String[] args) {

        int a = 20;

        System.out.println("a++ = " + a++); //20
        System.out.println("a = " + a); //21

        int b = 20;
        System.out.println("++b = " + ++b); //21

        int c = 20;
        System.out.println(++c + 1);




    }
}
