package day07_Operators;

public class ShortHandOperators {

    public static void main(String[] args) {

        //asignment: =
        int number = 100;

        System.out.println("number = " + number); //100

        number = 200; // Don't declare any other variable (int)
        System.out.println("number = " + number); //200, last initialised variable number will be given out



    }
}
